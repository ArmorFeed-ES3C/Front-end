<template>
  <div class="container flex flex-column space-between align-items-center">
    <div class="card flex-column my-5 flex">
      <pv-data-table
          ref="dt"
          :value="vehicles"
          datakey="id"
          :paginator="true"
          :rows="10"
          :filters="filters"
          paginatorTemplate="FirstPageLink PrevPageLink PageLinks NextPageLink LastPageLink CurrentPageReport RowsPerPageDropdown"
          :rowsPerPageOptions="[5, 10, 15]"
          currentPageReportTemplate="Showing {first} to {last} of {totalRecords} vehicles"
          responsiveLayout="stack"
      >
        <template #header>
          <div
              class="table-header flex flex-column md:flex-row md:justify-content-between"
          >
            <h5 class="mb-2 md:m-0 p-as-md-center text-xl">Vehicles</h5>
          </div>
          <pv-button
              label="New vehicle"
              icon="pi pi-plus"
              class="p-button-success mr-2"
              @click="openNew()"
          />
        </template>
        <pv-column
            field="brand"
            header="Brand"
            :sortable="true"
            style="min-width: 16rem"
        ></pv-column>
        <pv-column
            field="licensePlate"
            header="License Plate"
            :sortable="true"
            style="min-width: 16rem"
        ></pv-column>
        <pv-column
            field="year"
            header="Year"
            :sortable="true"
            style="min-width: 16rem"
        ></pv-column>

        <pv-column
            field="model"
            header="Model"
            :sortable="true"
            style="min-width: 16rem"
        ></pv-column>
        <pv-column
            field="maintenanceDate"
            header="Maintenance Date"
            :sortable="true"
            style="min-width: 16rem"
        ></pv-column>

        <pv-column :exportable="false" style="min-width: 8rem">
          <template #body="slotProps">
            <pv-button
                icon="pi pi-pencil"
                class="p-button-text p-button-rounded"
                @click="editVehicle(slotProps.data)"
            />
            <pv-button
                icon="pi pi-trash"
                class="p-button-text p-button-rounded"
            />
          </template>
        </pv-column>

        <pv-dialog
            v-model:visible="vehicleDialog"
            :style="{ width: '450px' }"
            header="Vehicle Information"
            :modal="true"
            class="p-fluid"
        >
          <div class="field mt-3">
            <span class="p-float-label">
              <pv-input-text
                  type="text"
                  id="brand"
                  v-model.trim="vehicle.brand"
                  required="true"
                  autofocus
                  :class="{ 'p-invalid': submitted && !vehicle.brand }"
              />
              <label for="brand">Brand</label>
              <small class="p-error" v-if="submitted && !vehicle.brand"
              >Brand is required</small
              >
            </span>
          </div>

          <div class="field">
            <span class="p-float-label">
              <pv-input-text
                  type="text"
                  id="licensePlate"
                  v-model="vehicle.licensePlate"
                  required="false"
                  rows="2"
                  cols="20"
              />
              <label for="licensePlate">License Plate</label>
              <small class="p-error" v-if="submitted && !vehicle.licensePlate"
              >License Plate is required</small
              >
            </span>
          </div>

          <div class="field">
            <span class="p-float-label">
              <pv-input-text
                  type="number"
                  id="year"
                  v-model="vehicle.year"
                  required="false"
                  rows="2"
                  cols="20"
              />
              <label for="year">Year</label>
              <small class="p-error" v-if="submitted && !vehicle.licensePlate"
              >Year is required</small
              >
            </span>
          </div>

          <div class="field">
            <span class="p-float-label">
              <pv-input-text
                  type="textarea"
                  id="model"
                  v-model="vehicle.model"
                  required="false"
                  rows="2"
                  cols="20"
              />
              <label for="model">Model</label>
              <small class="p-error" v-if="submitted && !vehicle.model"
              >Model is required</small
              >
            </span>
          </div>
          <div class="field">
            <span class="p-float-label">
              <pv-input-text
                  type="date"
                  id="maintenanceDate"
                  v-model="vehicle.maintenanceDate"
                  required="false"
                  rows="2"
                  cols="20"
              />

              <small
                  class="p-error"
                  v-if="submitted && !vehicle.maintenanceDate"
              >Date is required</small
              >
            </span>
          </div>

          <template #footer>
            <pv-button
                :label="'Cancel'.toUpperCase()"
                icon="pi pi-times"
                class="p-button-text"
                @click="hideDialog"
            />
            <pv-button
                :label="'Save'.toUpperCase()"
                icon="pi pi-check"
                class="p-button-text"
                @click="saveVehicle"
            />
          </template>
        </pv-dialog>
      </pv-data-table>
    </div>
  </div>
</template>

<script>
import { VehiclesApiService } from "../services/vehicle-api.service";
import { FilterMatchMode } from "primevue/api";

export default {
  name: "vehicle-list",
  data() {
    return {
      vehicles: [],
      vehicleDialog: false,
      vehicle: {},
      filters: {},
      submitted: false,
      vehicleService: null,
    };
  },
  created() {
    this.vehiclesService = new VehiclesApiService();
    this.vehiclesService.getAll().then((response) => {
      this.vehicles = response.data;
      this.vehicles.forEach((vehicle) => this.getDisplayableVehicle(vehicle));
    });
    this.initFilters();
  },
  methods: {
    getDisplayableVehicle(vehicle) {
      this.vehicle.id = vehicle.id;
      return vehicle;
    },
    getStorableVehicle(displayableVehicle) {
      return {
        id: displayableVehicle.id,
        brand: displayableVehicle.brand,
        licensePlate: displayableVehicle.licensePlate,
        year: displayableVehicle.year,
        model: displayableVehicle.model,
        maintenanceDate: displayableVehicle.maintenanceDate,
      };
    },
    openNew() {
      this.vehicles = {};
      this.submitted = false;
      this.vehicleDialog = true;
    },
    hideDialog() {
      this.vehicleDialog = false;
      this.submitted = false;
    },
    findIndexById(id) {
      return this.vehicles.findIndex((vehicle) => vehicle.id === id);
    },
    saveVehicle() {
      this.submitted = true;
      if (this.vehicle.brand.trim()) {
        if (this.vehicle.id) {
          this.vehicle = this.getStorableVehicle(this.vehicle);
          this.vehiclesService
              .update(this.vehicle.id, this.vehicle)
              .then((response) => {
                this.vehicles[this.findIndexById(response.data.id)] =
                    this.getDisplayableVehicle(response.data);
                this.$toast.add({
                  severity: "success",
                  summary: "Successful",
                  detail: "Vehicle Updated",
                  life: 3000,
                });
                console.log(response);
              });
        } else {
          this.vehicle.id = 0;
          console.log(this.vehicle);
          this.vehicle = this.getStorableVehicle(this.vehicle);
          this.vehicleService.create(this.vehicle).then((response) => {
            this.vehicle = this.getDisplayableVehicle(response.data);
            this.vehicle.push(this.vehicle);
            this.$toast.add({
              severity: "success",
              summary: "Successful",
              detail: "Vehicle Created",
              life: 3000,
            });
            console.log(response);
          });
        }
        this.vehicleDialog = false;
        this.vehicle = {};
      }
    },
    editVehicle(vehicle) {
      console.log(vehicle);
      this.vehicle = { ...vehicle };
      console.log(this.vehicle);
      this.vehicleDialog = true;
    },

    initFilters() {
      this.filters = {
        global: { value: null, matchMode: FilterMatchMode.CONTAINS },
      };
    },
  },
};
</script>

